//=============================================================================================
// Счетчик блокировок
//=============================================================================================
#include "LockCount.h"


//=============================================================================================
// Инициализация счетчика
//=============================================================================================
void LockCount_Init(TLockCount *pLC)  {

  *pLC = 0;
}

//=============================================================================================
// Блокировка стоит
//=============================================================================================
ULONG LockCount_IsLocked(TLockCount *pLC)  {

  return (ULONG) *pLC;
}

//=============================================================================================
// Добавить блокировку
//=============================================================================================
void LockCount_Lock(TLockCount *pLC)  {

  if (*pLC != -1)  ++*pLC;
}

//=============================================================================================
// Снять блокировку
//=============================================================================================
void LockCount_Unlock(TLockCount *pLC)  {

  if (*pLC)  --*pLC;
}
